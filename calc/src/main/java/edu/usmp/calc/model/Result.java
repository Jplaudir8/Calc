package edu.usmp.calc.model;

public class Result {

	private long n1; 
	private long n2;
	private long result;
	private char op;
	
	public long getN1() {
		return n1;
	}
	public void setN1(long n1) {
		this.n1 = n1;
	}
	public long getN2() {
		return n2;
	}
	public void setN2(long n2) {
		this.n2 = n2;
	}
	public long getResult() {
		return result;
	}
	public void setResult(long result) {
		this.result = result;
	}
	public char getOp() {
		return op;
	}
	public void setOp(char op) {
		this.op = op;
	}
	
}
